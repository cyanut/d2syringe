def decompress(s):
    b = 0
    size = len(s)
    count = 0x20
    ptr = 0
    res = []
    while(True):
        if (count >= 0x8):
            while size > 0 and count >= 8:
                count -= 0x8
                size -= 1
                a = s[ptr] << count
                a &= 0xFFFFFFFF
                ptr += 1
                b |= a
        index = index_table[b >> 0x18]
        a = character_table[index]
        d = (b >> (0x18 - a)) & bit_masks[a]
        c = character_table[index + 2 * d + 2]
        count += c
        if (count > 0x20):
            return "".join(res)
        a = character_table[index + 2 * d + 1]
        res.append(chr(a))
        b <<= (c & 0xFF)
        b &= 0xFFFFFFFF

def unpack_header(s):
    if s == b"\x01":
        return ("INIT", int(s), "")
    
    if len(s) > 4:
        header_word = struct.unpack("<H", s[:2])[0]
        if header_word == len(s):
            return ("MCP", int(s[2]), s[3:])

    if len(s) > 0:
        header_byte = int(s[0])
    
    if header_byte == len(s):
        sdecomp = decompress(s[1:])
        print("\\x"+"\\x".join([hex(ord(x))[2:] for x in sdecomp]))
        return("D2GS",ord(sdecomp[0]),sdecomp[1:])

    if header_byte == 0xFF and len(s) >= 4:
        if struct.unpack("<H", s[2:4])[0] == len(s):
            return ("SID", int(s[1]), s[4:])

    return ("D2GS", int(s[0]), s[1:])

def unpack(s, source):
    ptype = unpack_header(s) 
    pformat = d2protdata.protdic[source][ptype[0]][ptype[1]]
    args = pformat["args"]
    p = 0
    pstr = ptype[2]
    values = []
    for argtype in [x[0] for x in args]:
        argn = re.findall("\[([0-9]*)\]", argtype)
        if argn:
            if argn[0]:
                argn = int(argn[0])
            else:
                argn = -1
        else:
            argn = 1
        argv_list = []
        while (argn != 0) and (p < len(pstr)):    
            if argtype.find("BYTE") == 0:
                argv_list.append(pstr[p])
                p += 1
            elif argtype.find("WORD") == 0:
                argv_list.append(struct.unpack("<H", pstr[p:p+2])[0])
                p += 2
            elif argtype.find("DWORD") == 0:
                argv_list.append(struct.unpack("<I", pstr[p:p+4])[0])
                p += 4
            elif argtype.find("STRING") == 0:
                str_end = pstr.index(b"\x00", p)
                argv_list.append(pstr[p:str_end])
                p = str_end + 1
            else:
                break
            argn -= 1
        if len(argv_list) == 1:
            argv_list = argv_list[0]
        values.append(argv_list)
    newarg = [[x[0][0], x[1], x[0][1]] for x in zip(args, values)]
    

    return (ptype, pformat, newarg)
